    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public int getInsee() {
        return insee;
    }

    public void setInsee(int insee) {
        this.insee = insee;
    }

    public int getInseePere() {
        return inseePere;
    }

    public void setInseePere(int inseePere) {
        this.inseePere = inseePere;
    }

    public int getjNaiss() {
        return jNaiss;
    }

